    // creamos ya no de tipo list si no de la clase que creamos en este caso cliente
    List<Clientes> datos= new ArrayList<Clientes>();
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // iniciamos la clase que introducimos 
        Clientes cliente= new Clientes();
        // obtenemos datos de las cajas de texto y los agregamos a la clase que creamos
        cliente.setNombre(this.bxNombre.getText());
        cliente.setApellido(this.bxApellido.getText());
        cliente.setEmail(this.bxEmail.getText());
        cliente.setTelefono(this.bxTelefono.getText());
        // a√±adido los datos introducimos esta clase dentro de la lista creada a partir de una clase
        datos.add(cliente);
        // borramos los datos de las cajas de texto
        this.bxNombre.setText("");
        this.bxApellido.setText("");
        this.bxEmail.setText("");
        this.bxTelefono.setText("");
        // actualizamo los datos
         actualizarDatos();
    }//GEN-LAST:event_btnAceptarActionPerformed

    public void actualizarDatos()
    {
        // creamos una lstvariable con el que introduciremos dentro de la lista visible
        DefaultListModel lstVisible= new DefaultListModel();
        //obtenemos el valor de la lista y la recorremos para agrefar una a una
        for(int i =0;i<datos.size();i++)
        {
            // creamos una nueca clase que almacenara los datos que obtengamos de la lista de datos
            Clientes name=datos.get(i);
            // obtenida estos datos lo pasamos a la clase creada ara introducir datos a la lista visible
            lstVisible.addElement(name.getNombreCompleto());
        }
        // mandamos todos los datos creado a la lista visible
        this.lstUsuario.setModel(lstVisible);
    }
